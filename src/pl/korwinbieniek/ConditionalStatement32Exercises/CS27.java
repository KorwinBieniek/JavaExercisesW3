package pl.korwinbieniek.ConditionalStatement32Exercises;

public class CS27 {
    public void checkIf(int num)
    {
        if(num > 0)
            System.out.println("Integer is positive");
        else if(num < 0)
            System.out.println("Integer is negative");
        else
            System.out.println("Integer is zero");
    }
}
